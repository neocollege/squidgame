    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
        String gamename = null;
        String gname = game.getText();
        int partino, elino, gameid=0;
        partino = Integer.parseInt(partno.getText());
        elino = Integer.parseInt(elno.getText());
        
        try {
            // TODO add your handling code here:
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/squidgame?user=root", "root", "fS0c131ty#");
            ps = con.prepareStatement("select * from games where game_name = ?");
            ps.setString(1, gname);
            ResultSet rs2 = ps.executeQuery();
            while(rs2.next()){
                gamename = rs2.getString("game_name");
                gameid = rs2.getInt("game_id");
            }
            if(gamename != null){
                ps = con.prepareStatement("UPDATE games SET participants = ?, eliminated = ? WHERE game_id = ?");
                ps.setInt(1, partino);
                ps.setInt(2, elino);
                ps.setInt(3, gameid);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Game Stats Updated", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Game does not exist", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(enfScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateMouseClicked

