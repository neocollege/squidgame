    private void searchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchMouseClicked
        // TODO add your handling code here:
        String fname1=null;
        String lname1=null;
        fname = searchfName.getText();
        lname = searchlName.getText();
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/squidgame?user=root", "root", "fS0c131ty#");
            ps = con.prepareStatement("select fname, lname from players where(fname = ? AND lname = ?)");
            ps.setString(1, fname);
            ps.setString(2, lname);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                fname1 = rs.getString("fname");
                lname1 = rs.getString("lname");
            }
            if(fname1 == null){
                JOptionPane.showMessageDialog(this,"Player not found", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            else{
                playerProfile pfp = new playerProfile(fname1, lname1);
                pfp.setVisible(true);
                pfp.setLocationRelativeTo(null);
            }
        } catch (SQLException | IOException ex) {
            Logger.getLogger(enfScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_searchMouseClicked

